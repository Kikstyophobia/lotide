const countOnly = function(allItems, itemsToCount) {
  const results = {};
  // looping through list of names (allItems)
  for (let name of allItems) {
    if (itemsToCount[name]) {
      if (results[name]) {
        results[name] += 1;
      } else {
        results[name] = 1;
      }
    }
  }
  return results;
};


